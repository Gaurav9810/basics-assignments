// Q4. WAP for finding the volume of the cylinder by taking radius and height as input.
// Ans 4)
#include<iostream>
using namespace std;
int main(){
    int r,h,z;
    cout<<"enter the radius : ";//r = radius
    cin>>r;
    cout<<"enter the height : ";//h = height
    cin>>h;
    z = 3.14*r*r*h;
    cout<<"volume of the cylinder is : "<<z;
}


